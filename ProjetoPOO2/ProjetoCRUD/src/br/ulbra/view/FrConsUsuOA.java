    public void readJTableCons1() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbConsUsu.getModel();
        modelo.setNumRows(0);
        UsuarioDAO udao = new UsuarioDAO();
        for (Usuario p : udao.readConsAZ()) {
            modelo.addRow(new Object[]{
                p.getIdUsu(),
                p.getNomeUsu(),
                p.getEmailUsu(),
                p.getCidadeUsu(),
                p.getEstadoUsu()
            });
        }
    }
    public void readCons1ForDesc() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbConsUsu.getModel();
        modelo.setNumRows(0);
        UsuarioDAO pdao = new UsuarioDAO();
        for (Usuario p : pdao.readCons1ForDesc()) {
            modelo.addRow(new Object[]{
                p.getIdUsu(),
                p.getNomeUsu(),
                p.getEmailUsu(),
                p.getCidadeUsu(),
                p.getEstadoUsu()
            });
        }
    }

