    public void readConsVeicAZ() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbConsVeic.getModel();
        modelo.setNumRows(0);
        VeiculosDAO vdao = new VeiculosDAO();
        for (Veiculos v : vdao.readAZ()) {
            modelo.addRow(new Object[]{
                v.getcodVeic(),
                v.gettipoVeic(),
                v.getmarcaVeic(),
                v.getmodeloVeic(),
                v.getplacaVeic()
            });
        }
    }
