    private void btSalvarVeicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarVeicActionPerformed
        Veiculos v = new Veiculos();
        try {
            VeiculosDAO vd = new VeiculosDAO();
            v.settipoVeic(edTipoVeic.getText());
            v.setmarcaVeic(edMarcaVeic.getText());
            v.setmodeloVeic(edModeloVeic.getText());
            v.setanoFabVeic(Integer.parseInt(edAnoVeic.getText()));
            v.setcorVeic(edCorVeic.getText());
            v.setplacaVeic(edPlacaVeic.getText());
            v.setkmVeic(Integer.parseInt(edKmVeic.getText()));
            vd.create(v);
            readJTable();
            controlarBt(1);
}
       catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro:"+ex.getMessage());
}
    }//GEN-LAST:event_btSalvarVeicActionPerformed

