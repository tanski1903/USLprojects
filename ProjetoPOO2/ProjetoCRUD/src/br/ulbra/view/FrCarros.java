    private void btExcluirVeicActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirVeicActionPerformed
        Veiculos v = new Veiculos();
        try {
        VeiculosDAO vd = new VeiculosDAO();
        if(JOptionPane.showConfirmDialog(null,"Tem certeza que você deseja excluir o cadastro deste veículo?","Excluir Registro",JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            v.setcodVeic(Integer.parseInt(edCodVeic.getText()));
            vd.delete(v);
            readJTable();
            controlarBt(1);
}       else{
            JOptionPane.showMessageDialog(null, "Exclusão Cancelada com sucesso!");
}
}       catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
}
    }//GEN-LAST:event_btExcluirVeicActionPerformed

