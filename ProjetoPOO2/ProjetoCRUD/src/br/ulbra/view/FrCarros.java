    public void controlarBt(
            int op) {
        switch (op) {
            case 1:
                pnCadVeic.setVisible(false);
                limpar();
                break;
            case 2:
                pnCadVeic.setVisible(true);
                btSalvarVeic.setEnabled(true);
                btAlterarVeic.setEnabled(false);
                btExcluirVeic.setEnabled(false);
                break;
            case 3:
                pnCadVeic.setVisible(true);
                btAlterarVeic.setEnabled(true);
                btExcluirVeic.setEnabled(true);
                btSalvarVeic.setEnabled(false);
                break;
        }
        this.pack();
    }
    
    public void readJTable() throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbVeiculos.getModel();
        modelo.setNumRows(0);
        VeiculosDAO vdao = new VeiculosDAO();
        for (Veiculos v : vdao.read()) {
            modelo.addRow(new Object[]{
                v.getcodVeic(),
                v.gettipoVeic(),
                v.getmarcaVeic(),
                v.getmodeloVeic(),
                v.getanoFabVeic(),
                v.getcorVeic(),
                v.getplacaVeic(),
                v.getkmVeic()
            });
        }
    }
    public void readJTableForDesc(String nome) throws SQLException {
        DefaultTableModel modelo = (DefaultTableModel) tbVeiculos.getModel();
        modelo.setNumRows(0);
        VeiculosDAO vdao = new VeiculosDAO();
        for (Veiculos v : vdao.readForDesc(nome)) {
            modelo.addRow(new Object[]{
                v.getcodVeic(),
                v.gettipoVeic(),
                v.getmarcaVeic(),
                v.getmodeloVeic(),
                v.getanoFabVeic(),
                v.getcorVeic(),
                v.getplacaVeic(),
                v.getkmVeic()
            });
        }
    }
    
    
    

